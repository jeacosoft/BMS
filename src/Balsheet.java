    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String date = ((JTextField) trandate.getDateEditor().getUiComponent()).getText();
        try {
            // TODO add your handling code here:
            //
            String qry = "select * from  `balancesheetable` where Date = '"+date+"'";
            ps = con.prepareStatement(qry);
            rs = ps.executeQuery();
            if(rs.next()){
                try{

                    String sql2 = "UPDATE `balancesheetable` SET `CreditBalance_c_d`= (`Cash@Hand` +`TotalDeposit`), `DebitBalance_b_d`= (`TotalWithdrawal`+`TotalLoan`),`ClosingBalance_c_d`= (`CreditBalance_c_d` - `DebitBalance_b_d`)    where Date = '"+date+"'";
                    ps = con.prepareStatement(sql2);
                    int q = ps.executeUpdate();

                    if(q>0){

                        JOptionPane.showMessageDialog(this,"Account Balanced Successfully");
                        Balsheetdata();
                    }

                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this," Account Balancing failed");
                    Logger.getLogger(Balsheet.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(Balsheet.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

