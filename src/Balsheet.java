    private void btnsubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsubmitActionPerformed
        try {
            int resp = JOptionPane.showConfirmDialog(this, "Are you sure you want to Submit this? \nConfirm your entries before you go ahead.\nData submitted cannot be undone. ", "Warning",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE );
            String date = ((JTextField) trandate.getDateEditor().getUiComponent()).getText();
            String qry = "select * from  `balancesheetable` where Date = '"+date+"'";
            ps = con.prepareStatement(qry);
            rs = ps.executeQuery();

            if(!rs.next()){

                if(txtbalbf.getText().equals("")||txtobal.getText().equals("")){
                    JOptionPane.showMessageDialog(this,"Empty field(s) detected please fillin the field to proceed");

                }else{
                    try {

                        // Submiting the b/f,opening balance and the date

                        String sql = "INSERT INTO `balancesheetable`( `Balance_b_f`, `OpeningBalance`,  `TotalDeposit`, `TotalWithdrawal`, `TotalLoan`, `Date`) VALUES (?,?,?,?,?,?)";
                        ps = con.prepareStatement(sql);
                        ps.setString(1, txtbalbf.getText());
                        ps.setString(2, txtobal.getText());
                        ps.setDouble (3,0);
                        ps.setDouble (4,0);
                        ps.setDouble (5,0);
                        ps.setString(6,date);
                        if(resp == JOptionPane.YES_OPTION){
                            int k1 = ps.executeUpdate();
                            if(k1>0){

                                JOptionPane.showMessageDialog(this,"Submission was Successful");

                                Balsheetdata();
                            }
                        }else{}
                    } catch (SQLException ex) {

                        JOptionPane.showMessageDialog(this,"Submission Failed");
                        Logger.getLogger(Balsheet.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }else{
                try{

                    String sql2 = "UPDATE `balancesheetable` SET `OpeningBalance`= (`OpeningBalance` + '"+txtobal.getText()+"') where Date = '"+date+"'";
                    ps = con.prepareStatement(sql2);
                    int q = ps.executeUpdate();

                    if(q>0){

                        JOptionPane.showMessageDialog(this,"Updated was Successful");
                        Balsheetdata();
                    }

                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this,"Update failed");
                    Logger.getLogger(Balsheet.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

        } catch (SQLException ex) {
            Logger.getLogger(Balsheet.class.getName()).log(Level.SEVERE, null, ex);

        }

        //CALCULATING CASH AT HAND

        try{
            String date = ((JTextField) trandate.getDateEditor().getUiComponent()).getText();
            String sql2 = "UPDATE `balancesheetable` SET `Cash@Hand`= (`Balance_b_f` +`OpeningBalance`) where Date = '"+date+"'";
            ps = con.prepareStatement(sql2);
            int q = ps.executeUpdate();

            if(q>0){

                // JOptionPane.showMessageDialog(this,"Updated was Successful");
                Balsheetdata();
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this,"Update failed");
            Logger.getLogger(Balsheet.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnsubmitActionPerformed

