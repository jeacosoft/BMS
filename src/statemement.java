    private void txtacctnoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtacctnoKeyReleased
        // TODO add your handling code here:
        //searching and placing the corresponding value of the searched on the textboxes
        try {
            // displaying the corresponding value of selected combobox item on th textfied

            String sql ="select * from customertable where AccountNo = ?";
            ps = con.prepareStatement(sql);
            ps . setString(1,txtacctno.getText());
            rs = ps.executeQuery();
            if (rs.next()){
                String add1 = rs.getString("Branch");
                txtbranch.setText(add1);
                String add2 = rs.getString("AccountType");
                txtacctype.setText(add2);
                String add3 = rs.getString("AccountBalance");
                txtbalance.setText(add3);
                String add4 = rs.getString("Name");
                txtcstname.setText(add4);

            }
        } catch (SQLException ex) {
            Logger.getLogger(accountype.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_txtacctnoKeyReleased

    private void btnfindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnfindActionPerformed
        try {
            // //getting the account transaction between given date to given date (statement of account)

            String sql =  "select CustomerID, AccountNo, CustomerName,`ToAccountNo`,`ToAccountName`,AccountType, Amount, AccountBalance,TransactionType,Date from transactiontable where Date between '"+Datfrom.getDate ()+"' and '"+Dateto.getDate()+"' and AccountNo = '"+txtacctno.getText()+"'";

            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            if (rs.next()){
                tblactstatement.setModel(DbUtils.resultSetToTableModel(rs));

            }
        } catch (SQLException ex) {
            Logger.getLogger(statemement.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnfindActionPerformed

